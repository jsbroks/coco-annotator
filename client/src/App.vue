<template>
  <div id="app">
    <NavBar v-show="showNavBar"/>
    <RouterView :key="$route.fullPath" />
  </div>
</template>

<script>
import NavBar from "@/components/NavBar";
import { mapMutations } from "vuex";

export default {
  name: "App",
  components: { NavBar },
  methods: {
    ...mapMutations("user", ["setUserInfo"]),
    ...mapMutations("info", ["getServerInfo"]),
    toAuthPage() {
      this.$router.push({
        name: "authentication"
      });
    }
  },
  computed: {
    showNavBar() {
      let notShow = ["authentication", "setup"];
      return notShow.indexOf(this.$route.name) === -1;
    },
    isAuthenticated() {
      return this.$store.state.user.isAuthenticated;
    },
    isAuthenticatePending() {
      return this.$store.state.user.isAuthenticatePending;
    },
    loginRequired() {
      if (this.isAuthenticatePending) {
        return false;
      }
      return !this.isAuthenticated;
    }
  },
  watch: {
    loginRequired(newValue) {
      if (newValue) {
        this.toAuthPage();
        return;
      }

      if (this.$route.name == "authentication" && !newValue) {
        this.$router.push({
          name: "datasets"
        });
      }
    },
    "$route.name"() {
      if (this.loginRequired) {
        this.toAuthPage();
      } else {
        if (this.$route.name === "authentication") {
          this.$router.push({ name: "datasets" });
        }
      }
    }
  },
  created() {
    this.setUserInfo();
    this.getServerInfo();
  }
};
</script>

